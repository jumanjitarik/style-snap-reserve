export const EMOJI_PREFIX = "emoji:";

export function isEmojiIcon(v: string | null | undefined): v is string {
  return !!v && v.startsWith(EMOJI_PREFIX);
}

export function CategoryIcon({
  icon,
  className,
  alt = "",
  emojiClassName,
}: {
  icon: string | null | undefined;
  className?: string;
  alt?: string;
  emojiClassName?: string;
}) {
  if (isLucideIcon(icon)) {
    const name = icon.slice(LUCIDE_PREFIX.length);
    const LucideCmp = (lucideIcons as unknown as Record<string, React.ComponentType<{ className?: string; strokeWidth?: number; "aria-label"?: string }>>)[name];
    if (LucideCmp) {
      return (
        <LucideCmp
          aria-label={alt || undefined}
          strokeWidth={1.75}
          className={cn("text-primary", className)}
        />
      );
    }
    return <CategoryFallbackIcon className={cn("text-primary", className)} />;
  }
  if (isEmojiIcon(icon)) {
    return (
      <span
        aria-label={alt || undefined}
        className={cn("inline-flex items-center justify-center leading-none", className, emojiClassName)}
      >
        {icon.slice(EMOJI_PREFIX.length)}
      </span>
    );
  }
  if (icon) {
    return <SafeImg src={icon} alt={alt} className={cn("object-contain", className)} />;
  }
  return <CategoryFallbackIcon className={cn("text-primary", className)} />;
}

