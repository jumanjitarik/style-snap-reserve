import { useEffect, useState } from "react";
import type { User } from "@supabase/supabase-js";
import { supabase } from "@/integrations/supabase/client";

/**
 * Reactive current-user hook. Subscribes to auth state changes so consumers
 * re-render on sign-in/sign-out without ad-hoc supabase.auth.getUser() calls.
 *
 * For imperative one-shot reads (event handlers, submit callbacks) keep using
 * `await supabase.auth.getUser()` directly — this hook is for render-time state.
 */
export function useCurrentUser() {
  const [user, setUser] = useState<User | null>(null);
  const [loading, setLoading] = useState(true);

  useEffect(() => {
    let active = true;
    supabase.auth.getUser().then(({ data }) => {
      if (!active) return;
      setUser(data.user ?? null);
      setLoading(false);
    });
    const { data: sub } = supabase.auth.onAuthStateChange((_event, session) => {
      if (!active) return;
      setUser(session?.user ?? null);
      setLoading(false);
    });
    return () => {
      active = false;
      sub.subscription.unsubscribe();
    };
  }, []);

  return { user, userId: user?.id ?? null, loading };
}
